//! Emits the MCP tool descriptions from the vendored Oura spec (#10) — the same
//! build-time spec-read pattern as `oura-toolkit-auth`'s OAuth metadata (nothing textual
//! about the API is hardcoded where the spec can supply it).
//!
//! Each curated tool's description = a hand-written LLM-facing lead (curation is the
//! point — the spec's summaries are terse API-doc prose) + the operation's spec summary +
//! the response document's field inventory WITH the spec's own field descriptions. The
//! field inventory auto-tracks `just spec-fetch` refreshes; a curated operation
//! disappearing from the spec fails the build (drift guard, not silent decay).
//!
//! The spec is read from the crate-local bundle `openapi.json` (what ships in the
//! crates.io package; kept in sync with the repo root's `spec/openapi.json` by
//! `just spec-fetch` + the bundled-spec test), falling back to a repo-root walk.

use std::{env, fs, path::PathBuf};

